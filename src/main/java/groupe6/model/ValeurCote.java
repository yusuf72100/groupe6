package groupe6.model;

/**
 * L'énumération des valeurs possibles pour les cotés d'une cellule.
 * Les valeurs possibles sont VIDE, TRAIT et CROIX.
 *
 * @author Yamis
 */
public enum ValeurCote {
  /**
   * Valeur : vide
   */
  VIDE,
  /**
   * Valeur : trait
   */
  TRAIT,
  /**
   * Valeur : croix
   */
  CROIX
}