package groupe6.model;

/**
 * Enumération qui correspond à la difficulté (complexité) technique d'un puzzle
 * Les valeurs possibles sont DEMARRAGE, BASIQUE et AVANCEE
 *
 * @author Nathan
 */
public enum DifficulteTechnique {
    /**
     * Difficulté : démarrage
     */
    DEMARRAGE,
    /**
     * Difficulté : basique
     */
    BASIQUE,
    /**
     * Difficulté : avancée
     */
    AVANCEE;
}