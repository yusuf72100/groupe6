package groupe6.model;

/**
 * Enumération qui correspond à la direction d'un déplacement
 * Les valeurs possibles sont ADJACENT, DIAGONAL et TOUTEDIRECTION
 *
 * @author Matéo
 */
public enum Direction {
  /**
   * Direction : adjacent
   */
  ADJACENT,
  /**
   * Direction : diagonal
   */
  DIAGONAL,
  /**
   * Direction : toute direction
   */
  TOUTEDIRECTION;
}
