package groupe6.model.partie.puzzle.cellule;

/**
 * L'énumération des valeurs possibles pour les côtés d'une cellule.
 * Les valeurs possibles sont : VIDE, TRAIT et CROIX.
 *
 * @author Yamis
 */
public enum ValeurCote {
  /**
   * Valeur : vide
   */
  VIDE,
  /**
   * Valeur : trait
   */
  TRAIT,
  /**
   * Valeur : croix
   */
  CROIX
}