package groupe6;

/**
 * Classe contenant les codes ANSI pour les couleurs.
 *
 * @author Yamis
 */
public class CouleursANSI {

  /**
   * Code ANSI pour r√©initialiser la couleur du texte.
   */
  public static final String RESET = "\u001B[0m";

  /**
   * Code ANSI pour afficher du texte en vert.
   */
  public static final String GREEN = "\u001B[32m";

  /**
   * Code ANSI pour afficher du texte en jaune.
   */
  public static final String YELLOW = "\u001B[33m";

  /**
   * Code ANSI pour afficher du texte en rouge.
   */
  public static final String RED = "\u001B[31m";

  /**
   * Code ANSI pour afficher du texte en bleu.
   */
  public static final String BLUE = "\u001B[34m";

  /**
   * Code ANSI pour afficher du texte en bleu gras.
   */
  public static final String BLUE_BOLD = "\u001B[1;34m";

}
